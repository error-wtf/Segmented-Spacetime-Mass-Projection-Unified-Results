            c = 299792458  # m/s
            
            T_proxy = (h_bar * kappa_median) / (2 * np.pi * k_B * c)
            
            print("\n" + "="*80)
            print("PREDICTION 4: NATURAL HAWKING RADIATION")
            print("="*80)
            print(f"Horizon radius r_œÜ: {r_phi:.4e} m")
            print(f"Analysis window: ¬±{100*0.05:.0f}% around r_œÜ")
            print(f"Points in window: {len(df_window)}")
            print(f"\nSurface Gravity Proxy Œ∫_seg:")
            print(f"  Œ∫_seg (median) = {kappa_median:.4e} m‚Åª¬π")
            print(f"  Œ∫_seg (mean)   = {kappa_mean:.4e} m‚Åª¬π")
